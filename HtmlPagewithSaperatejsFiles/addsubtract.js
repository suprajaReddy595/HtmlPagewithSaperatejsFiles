function add() {

    var x = parseInt(document.getElementById("txtNo1").value);
    var y = parseInt(document.getElementById("txtNo2").value);
    document.getElementById("txtResult").value = x + y;

}
function subtract() {

    var x = parseInt(document.getElementById("txtNo1").value);
    var y = parseInt(document.getElementById("txtNo2").value);
    document.getElementById("txtResult").value = x - y;

}